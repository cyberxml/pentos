# Squirrel SQL - database browswer

cd /opt/pentos/apps
mkdir /opt/pentos/apps/squirrelsql
cd /opt/pentos/apps/squirrelsql

wget http://downloads.sourceforge.net/project/squirrel-sql/1-stable/3.7.0/squirrel-sql-3.7-standard.jar

echo java -jar /opt/pentos/apps/squirrelsql/squirrel-sql-3.7-standard.jar > squirrelsql.sh

chmod 755 squirrelsql.sh

